function drawGrid(cols, rows) {
  for (let i = 1; i <= cols * rows; i++) {
    grid_item = document.createElement("div");
    grid_container.appendChild(grid_item);
    grid_item.classList.add('grid-item');
    grid_item.style.height = 900 / rows + "px";
    grid_item.style.width = 900 / cols + "px";
    colorOn = false;
    // Add event listener to each grid-item
    grid_item.addEventListener('mouseover', changeColor);


  }
}

function changeColor(event) {
  const clickedItem = event.target;
  clickedItem.style.backgroundColor = colors[Math.floor(Math.random()*colors.length)];
  // colorOn = true;
}



